package org;

public class Aresta {

	public int ini;
	public int fim;

	public Aresta() {
	}

	public Aresta(int ini, int fim) {
		this.ini = ini;
		this.fim = fim;
	}

	public int getIni() {
		return ini;
	}

	public void setIni(int ini) {
		this.ini = ini;
	}

	public int getFim() {
		return fim;
	}

	public void setFim(int fim) {
		this.fim = fim;
	}

}
